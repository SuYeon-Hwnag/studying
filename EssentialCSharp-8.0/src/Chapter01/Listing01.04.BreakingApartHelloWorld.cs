namespace AddisonWesley.Michaelis.EssentialCSharp.Chapter01.Listing01_04
{
    public class Program//Class definition 클래스 구현
    {
        public static void Main()//Method declaration 메소드 구현 메인 밖 안읽음
        {
            System.Console.WriteLine("Hello, My name is Inigo Montoya");//This is the statement 구문 
            System.Console.Write("Hello, My name is Inigo Montoya");// write는 그냥 쭉 쓰는거
            System.Console.WriteLine("Hello, My name is Inigo Montoya"); // writeline 엔터
        }//This is the end of the method
    }//end of the class definition
}
